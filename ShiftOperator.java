package tw.aaa.myproject.OperatorsTest;

public class ShiftOperator {

	public static void main(String[] args) {
int a=1234;
System.out.println("1234>>2=" + (a>>2));
System.out.println("-1234>>2=" + (-a>>2));
System.out.println("1234<<2=" + (a<<2));
System.out.println("-1234<<2=" + (-a<<2));
System.out.println("1234>>>2=" + (a>>>2));
System.out.println("-1234>>>2=" + (-a>>>2));
System.out.println("6>>1=" + (6>>1));
	}

}
