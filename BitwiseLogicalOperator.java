package tw.aaa.myproject.OperatorsTest;
//邏輯運算式
public class BitwiseLogicalOperator {

	public static void main(String[] args) {
		int x=2,y=3;
		System.out.println("~"+x+"="+(~x));
		System.out.println(x+"&"+y+"="+(x&y));
		System.out.println(x+"|"+y+"="+(x|y));
		System.out.println(x+"^"+y+"="+(x^y));
	}

}
